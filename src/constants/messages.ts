export const MODAL_ACTIONS = {
  OPEN_MODAL: 'OPEN_MODAL',
  OPEN_CONTEXT_MENU_MODAL: 'OPEN_CONTEXT_MENU_MODAL',
} as const;

export type ModalAction = typeof MODAL_ACTIONS[keyof typeof MODAL_ACTIONS];

/**
 * 메시지 액션 타입
 */
export type MessageAction = typeof MESSAGE_ACTIONS[keyof typeof MESSAGE_ACTIONS];

/**
 * 기본 메시지 인터페이스
 */
export interface Message<T = unknown> {
  action: MessageAction;
  data?: T;
  timestamp: number;
}

/**
 * 메시지 응답 인터페이스
 */
export interface MessageResponse<T = unknown> {
  success: boolean;
  data?: T;
  error?: string;
}

/**
 * Modal Bridge 메시지 타입
 */
export const MODAL_MESSAGE_TYPES = {
  READY: 'klic:modal-ready',
  CLOSE: 'klic:modal-close',
  CLOSE_REQUEST: 'klic:modal-close-request',
  SET_TOOL: 'klic:modal-set-tool',
  PARENT_READY: 'klic:modal-parent-ready',
} as const;

export type ModalMessageType = (typeof MODAL_MESSAGE_TYPES)[keyof typeof MODAL_MESSAGE_TYPES];

export interface ModalMessageBase {
  type: ModalMessageType;
  nonce: string;
}

export interface ModalCloseMessage extends ModalMessageBase {
  type: typeof MODAL_MESSAGE_TYPES.CLOSE;
  reason?: string;
}

export interface ModalReadyMessage extends ModalMessageBase {
  type: typeof MODAL_MESSAGE_TYPES.READY;
}

export interface ModalCloseRequestMessage extends ModalMessageBase {
  type: typeof MODAL_MESSAGE_TYPES.CLOSE_REQUEST;
}

export interface ModalSetToolMessage extends ModalMessageBase {
  type: typeof MODAL_MESSAGE_TYPES.SET_TOOL;
  tool?: string | null;
}

export interface ModalParentReadyMessage extends ModalMessageBase {
  type: typeof MODAL_MESSAGE_TYPES.PARENT_READY;
}

export interface ModalErrorMessage extends ModalCloseMessage {
  reason: string;
}

export interface ModalOpenMessage {
  action: typeof MODAL_ACTIONS.OPEN_MODAL | typeof MODAL_ACTIONS.OPEN_CONTEXT_MENU_MODAL;
  tool?: string | null;
  tabId?: number;
  nonce: string;
}

export type ModalMessage =
  | ModalReadyMessage
  | ModalCloseRequestMessage
  | ModalSetToolMessage
  | ModalCloseMessage
  | ModalParentReadyMessage;
